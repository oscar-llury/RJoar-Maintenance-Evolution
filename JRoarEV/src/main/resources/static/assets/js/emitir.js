
$(document).ready(function(){
       	$("#show-button-1").click(function(){
          
            var valor = $(this).val();
          
            if($(this).is(':checked')){
                $("#show-div-1").css("display", "block");
           
            }else{
                $("#show-div-1").css("display", "none");
                
            }
    });
});


